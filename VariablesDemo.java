class VariablesDemo {
	public static void main(String[] args) {
		int a=4; // intialization
		System.out.println(a);
		
		a=5; //Assignment
		System.out.println(a);
		
		int i=10;
		double d=5.5;
		char c='S';
		boolean b=true;
		
		System.out.println("Integer: " +i);
		System.out.println("Double: " +d);
		System.out.println("Character: " +c);
		System.out.println("Boolean: " +b);	
	}
}