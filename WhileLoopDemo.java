class WhileLoopDemo {
	
	public static void main(String[] args) {
		int i=0;
		int j=10;
		System.out.println("Printing 1 to 10 numbers");
		while(i<=10){
			System.out.println(i);
			i++;
		}
		System.out.println("Printing reverse of numbers");
		while(j>=0){
			System.out.println(j);
			j--;
		}
	}
}
		