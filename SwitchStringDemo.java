class SwitchStringDemo {
	
	public static void main(String[] args) {
		
		char opt='I';
		switch(opt){
			case 'I' : System.out.println("India");
						break;	
			case 'A' : System.out.println("America");
						break;
			case 'S' : System.out.println("Switzerland");
						break;			
			default : System.out.println("Mars");				
		}
	}
}
		